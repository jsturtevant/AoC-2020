func Test_countValidPassprts(t *testing.T) {
	type args struct {
		input string
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{
			name: "given example",
			args: args{
				input: input,
			},
			want: 2,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := countValidPassprts(tt.args.input); got != tt.want {
				t.Errorf("countValidPassprts() = %v, want %v", got, tt.want)
			}
		})
	}
}
